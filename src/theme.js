/**
 * Theme Definitions
 *
 * Each theme is a flat object of design tokens consumed by styled-components
 * via <ThemeProvider>.  Components access tokens as `${({ theme }) => theme.text}`.
 *
 * Token overview:
 *  body / text / dark       — page background, primary text, darkest shade
 *  secondaryText            — muted text for descriptions
 *  accentColor / accentBright — brand accent (buttons, highlights)
 *  projectCard / imageDark  — card & image container backgrounds
 *  skinColor / skinColor2   — avatar SVG skin tones
 *  imageClothes / avatarMisc / avatarShoes — avatar SVG details
 *
 * Additional semantic tokens prepared for later UI refinement:
 *  bodyAlt / cardBackgroundAlt — alternate surface backgrounds
 *  accentSoft / glowColor      — softer accent fills and ambient glow
 *  borderSoft                  — low-emphasis separators
 *  buttonText                  — readable text/icon color on buttons
 *  shadowColor                 — shared elevated surface shadow tone
 *  heroGradient                — premium hero/backdrop gradient
 *
 * Optional immersive tokens for selective high-identity themes:
 *  surfaceRadius / heroRadius / controlRadius / pillRadius
 *  panelBorderWidth / panelBorderStyle
 *  accentFontFamily / accentLetterSpacing
 *  headerSurface / headerPattern / heroPattern / surfacePattern / buttonPattern
 *  panelGlow / buttonGlow
 */

function expandHex(hex) {
  if (hex.length === 4) {
    return `#${hex[1]}${hex[1]}${hex[2]}${hex[2]}${hex[3]}${hex[3]}`;
  }

  return hex;
}

function hexToRgb(hex) {
  const normalizedHex = expandHex(hex).replace("#", "");
  const numericHex = Number.parseInt(normalizedHex, 16);

  return {
    r: (numericHex >> 16) & 255,
    g: (numericHex >> 8) & 255,
    b: numericHex & 255,
  };
}

function withAlpha(hex, alpha) {
  const { r, g, b } = hexToRgb(hex);
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}

function mixHex(hexA, hexB, ratio) {
  const colorA = hexToRgb(hexA);
  const colorB = hexToRgb(hexB);

  const mixChannel = (channelA, channelB) =>
    Math.round(channelA + (channelB - channelA) * ratio)
      .toString(16)
      .padStart(2, "0");

  return `#${mixChannel(colorA.r, colorB.r)}${mixChannel(colorA.g, colorB.g)}${mixChannel(
    colorA.b,
    colorB.b
  )}`.toUpperCase();
}

function getReadableTextColor(backgroundHex) {
  const { r, g, b } = hexToRgb(backgroundHex);
  const brightness = (r * 299 + g * 587 + b * 114) / 1000;

  return brightness > 150 ? "#0F172A" : "#F8FAFC";
}

function createThemeTokens(family, mode, tokens) {
  const isLightMode = mode === "light";
  const bodyAlt =
    tokens.bodyAlt ??
    mixHex(tokens.body, tokens.projectCard, isLightMode ? 0.42 : 0.58);
  const cardBackgroundAlt =
    tokens.cardBackgroundAlt ??
    mixHex(tokens.projectCard, tokens.imageDark, isLightMode ? 0.3 : 0.6);
  const borderSoft =
    tokens.borderSoft ??
    withAlpha(tokens.borderColor, isLightMode ? 0.55 : 0.78);
  const accentSoft =
    tokens.accentSoft ??
    withAlpha(tokens.accentColor, isLightMode ? 0.14 : 0.22);
  const shadowColor =
    tokens.shadowColor ??
    (isLightMode ? withAlpha(tokens.text, 0.1) : "rgba(0, 0, 0, 0.34)");
  const glowColor =
    tokens.glowColor ??
    withAlpha(tokens.accentBright, isLightMode ? 0.24 : 0.34);
  const heroGradient =
    tokens.heroGradient ??
    `linear-gradient(135deg, ${tokens.body} 0%, ${bodyAlt} 58%, ${withAlpha(
      tokens.accentBright,
      isLightMode ? 0.12 : 0.18
    )} 100%)`;

  return {
    family,
    name: mode,
    body: tokens.body,
    text: tokens.text,
    dark: tokens.dark ?? "#000000",
    secondaryText: tokens.secondaryText,
    accentColor: tokens.accentColor,
    accentBright: tokens.accentBright,
    projectCard: tokens.projectCard,
    skinColor: tokens.skinColor ?? "#F7B799",
    skinColor2: tokens.skinColor2 ?? "#FCB696",
    imageDark: tokens.imageDark,
    imageClothes: tokens.imageClothes,
    avatarMisc: tokens.avatarMisc,
    avatarShoes: tokens.avatarShoes,
    borderColor: tokens.borderColor,
    buttonColor: tokens.buttonColor,
    hoverColor: tokens.hoverColor,
    headerColor: tokens.headerColor,
    selectorBackground: tokens.selectorBackground,
    selectorText: tokens.selectorText,
    bodyAlt,
    accentSoft,
    cardBackgroundAlt,
    borderSoft,
    buttonText: tokens.buttonText ?? getReadableTextColor(tokens.buttonColor),
    shadowColor,
    heroGradient,
    glowColor,
    surfaceRadius: tokens.surfaceRadius ?? "24px",
    heroRadius: tokens.heroRadius ?? "30px",
    controlRadius: tokens.controlRadius ?? "16px",
    pillRadius: tokens.pillRadius ?? "999px",
    panelBorderWidth: tokens.panelBorderWidth ?? "1px",
    panelBorderStyle: tokens.panelBorderStyle ?? "solid",
    accentFontFamily: tokens.accentFontFamily ?? '"Google Sans Medium", sans-serif',
    accentLetterSpacing: tokens.accentLetterSpacing ?? "normal",
    headerSurface: tokens.headerSurface ?? "transparent",
    headerPattern: tokens.headerPattern ?? "none",
    heroPattern: tokens.heroPattern ?? "none",
    surfacePattern: tokens.surfacePattern ?? "none",
    buttonPattern: tokens.buttonPattern ?? "none",
    panelGlow: tokens.panelGlow ?? "none",
    buttonGlow: tokens.buttonGlow ?? "none",
  };
}

function createThemeFamily(family, lightTokens, darkTokens) {
  return {
    light: createThemeTokens(family, "light", lightTokens),
    dark: createThemeTokens(family, "dark", darkTokens),
  };
}

